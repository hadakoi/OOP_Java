// Define a public class named Example3
public class Example3 {

// The main method is the entry point of any Java application
public static void main(String[] args) {

   // Declare an integer variable named w
   int w;

   // Declare a floating-point variable named x
   double x;

   // Assign the value 10 to the integer variable w
   w = 10;

   // Assign the value 10.0 to the floating-point variable x
   x = 10.0;

   // Print the original value of w to the console
   System.out.println("Original value of w: " + w);

   // Print the original value of x to the console
   System.out.println("Original value of x: " + x);

   // Print a blank line for better readability
   System.out.println();

   // Divide the value of w by 4 and assign the result back to w
   w = w / 4;

   // Divide the value of x by 4 and assign the result back to x
   x = x / 4;

   // Print the value of w after division to the console
   System.out.println("w after division: " + w);

   // Print the value of x after division to the console
   System.out.println("x after division: " + x);

   }
}
