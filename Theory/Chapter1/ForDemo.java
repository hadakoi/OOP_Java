class ForDemo {
   public static void main(String[] args) {
      int count;
      
      // Use a for loop to count from 0 to 4
      for (count = 0; count < 5; count++) {
         System.out.println("This is count: " + count);
      }

      // Print a message after the loop completes
      System.out.println("Done!");
   }
}
