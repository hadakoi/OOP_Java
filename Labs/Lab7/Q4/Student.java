public class Student {
  private int rollNo;
  private int marks;

  public void putNumber(int rollNo) {
    this.rollNo = rollNo;
  }
  public int getNumber() {
    return rollNo;
  }

  public void putMarks(int marks) {
    this.marks = marks;
  }

  public int getMarks() {
    return marks;
  }
}
